/* Map ASCII character to glyph index, or -1 if unsupported. */
static inline int
ag_font_index (char c)
{
    if (c == ' ')                  return 0;
    if (c == '.')                  return 1;
    if (c == ':')                  return 2;
    if (c == ';')                  return 3;
    if (c == '\'')                 return 4;
    if (c == '-')                  return 5;
    if (c == '=')                  return 6;
    if (c == '[')                  return 7;
    if (c == ']')                  return 8;
    if (c >= '0' && c <= '9')     return 9 + (c - '0');
    if (c >= 'a' && c <= 'z')     return 19 + (c - 'a');
    if (c >= 'A' && c <= 'Z')     return 19 + (c - 'A');  /* uppercase→lowercase */
    return -1;
}

/*
 * Render a text string onto an SDL renderer using the bitmap font.
 * Each pixel is drawn as a (scale × scale) filled rectangle.
 * Glyph spacing: 6 × scale pixels (5 glyph + 1 gap).
 */
static inline void
ag_font_render (SDL_Renderer *renderer, const char *text,
                int x, int y, int scale,
                uint8_t r, uint8_t g, uint8_t b)
{
    SDL_SetRenderDrawColor (renderer, r, g, b, 255);

    int cx = x;
    for (const char *p = text; *p; p++) {
        int idx = ag_font_index (*p);
        if (idx < 0) {
            cx += 6 * scale;
            continue;
        }

        const uint8_t *glyph = ag_font_glyphs[idx];
        for (int row = 0; row < 7; row++) {
            uint8_t bits = glyph[row];
            for (int col = 0; col < 5; col++) {
                if (bits & (0x80 >> col)) {
                    SDL_Rect rc = {
                        cx + col * scale,
                        y  + row * scale,
                        scale, scale
                    };
                    SDL_RenderFillRect (renderer, &rc);
                }
            }
        }
        cx += 6 * scale;
    }
}

